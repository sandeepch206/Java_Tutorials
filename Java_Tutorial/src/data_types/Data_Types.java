package data_types;

public class Data_Types {

	public static void main(String[] args) 
	{
		/*boolean Data Type
		The Boolean data type is used to store only 
		two possible values: TRUE and False 
		ex: boolean one = false*/
		
		boolean x = true;
		boolean y = false;
		
		System.out.println(x);
		System.out.println(y);
		
		
		/*Byte Data Type
		1. The Byte data type is used to store numeric values
		2. Its value range lies between -128 to 127(inclusive)
		ex: byte a = 10, byte b = -20 */
		
		byte a = 126;
		byte b = -127;
		
		System.out.println(a);
		System.out.println(b);
		
		/*short Data Type
		 1. The Short data type is used to store numeric values
		 2. Its value range lies between -32,768 to 32,767 (inclusive)
		 ex: short s = 10000, short r = -5000 */
		
		short c = 4509;
		short d = -30057;
		
		System.out.println(c);
		System.out.println(d);
		
		/*int Data Type
		1. The Int data type is used to store numeric values.
		2. Its value range lies between - 2,147,483,648 (-2^31) 
		to 2,147,483,647 (2^31 -1) (inclusive)
		ex: int a = 100000, int b = -200000 */
		
		int e = -2347894;
		int f = 34567892;
		
		System.out.println(e);
		System.out.println(f);
		
		/*Long Data Type
		1. The Long data type is used to store numeric values.
		2. Its value range lies between -9,223,372,036,854,775,808(-2^63) to 
		9,223,372,036,854,775,807(2^63 -1)(inclusive).
		ex: long a = 100000L, long b = -200000L */
		
		long g = -2347896373834L;
		long h = 3456789637732L;
		
		System.out.println(g);
		System.out.println(h);
		
		/*Float Data Type
		1. The Float data type is used to store numeric values.
		2. Its value range is unlimited.
		3. Its default value is 0.0F.
		ex: float f1 = 234.5f*/
		
		float i = 456.7f;
		System.out.println(i);
		
		/*Double Data Type
		1. The Double data type is used to store numeric values.
		2. Its value range is unlimited.
		3. Its default value is 0.0d.
		ex:double d1 = 12.3 */
		
		double j = 345.5;
		System.out.println(j);
		
		/*Char Data Type
		1. The Char data type is used to store single characters (letters, digits, symbols, 
				etc...).
		2. Its value-range lies between '\u0000' (or 0) to '\uffff' (or 65,535 inclusive)
		ex: char letterA = 'A'*/
		
		char k = 'A';
		System.out.println(k);

		
		

	}

}
