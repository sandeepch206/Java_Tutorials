package operators;

public class Arithmetic_Operators {

	public static void main(String[] args) 
	{
		int a = 10;
		int b = 35;
		double c = a;
		
		int add = a+b;
		double sub = c-a;
		double mlt = b*c;
		int div = b/a;
		double quo = c%a;
		
		System.out.println(add);
		System.out.println(sub);
		System.out.println(mlt);
		System.out.println(div);
		System.out.println(quo);
		
		
	

	}

}
